import java.lang.*;
import java.util.*;

class cuboid{

  public static void main(String[] args) {
    
    int x=6;
    int y;
    y = x++;
    System.out.println("y = "+ y);
    System.out.println("x = "+x);
    y = ++x;
    System.out.println("y = "+ y);
    System.out.println("x = "+x);

  }

}
